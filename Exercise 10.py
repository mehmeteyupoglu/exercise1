"""
Exercise 10

Take two lists and write a program that returns a list that contains only 
the elements that are common between randomly generated lists (without duplicates). 
Make sure your program works on two lists of different sizes.

"""

from random import sample

a = sample(range(1,20), 13)
b = sample(range(1,30), 18)

c = [i for i in set(a) if i in b]
print(c)

